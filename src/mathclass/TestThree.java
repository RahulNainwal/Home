package mathclass;

/*
 * The java.lang.Math.floor() returns the double value that is less than
 *  or equal to the argument and is equal to the nearest mathematical integer.
 */
public class TestThree {
	public static void main(String[] args) {
		double d = 34354, e = 0.00, f = 5.7, g = -6.7, h = 4.1, i = 6.9;
		System.out.println(Math.floor(d));
		System.out.println(Math.floor(e));
		System.out.println(Math.floor(f));
		System.out.println(Math.floor(g));
		System.out.println(Math.floor(h));
		System.out.println(Math.floor(i));
	}
}
